// A. REGISTER THE MODULE +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

var globalController = angular.module('globalController', []);

// A. END +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

// B. CONTROLLER ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

globalController.controller(
    'ctrlGlobal', [ 
        '$scope',
        '$state',
        '$location',
        function($scope, $state, $location) {
            
            // B.1. HELPER VARIABLES ----------------------------------------------------------------------------------
            
            $scope.snippets = 'assets/includes/snippets/';
            $scope.sections = 'assets/includes/sections/';
            $scope.icon = 'assets/images/icons';
            

            // B.2. MOBILE LISTENER -----------------------------------------------------------------------------------

            var screen = $( window ).width();
            console.log('I am a screen, and my width is: ' + screen);

            // B.2. END -----------------------------------------------------------------------------------------------

            // B.3. ANCHOR SCROLL -------------------------------------------------------------------------------------
            
            $scope.scrollTo = function(id) {
                
                $location.hash(id);
                console.log($location.hash());
                $anchorScroll();
                
            };

            // B.3. END -----------------------------------------------------------------------------------------------

        }
    ]
);

// B. END +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

